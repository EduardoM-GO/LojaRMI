    private String Tipo(int s) {
        if (s == 0) {
            return "TV";
        } else if (s == 1) {
            return "Fog√£o";
        } else if (s == 2) {
            return "Celular";
        } else if (s == 3) {
            return "Fone";
        } else {
            return "Lavadora";
        }
    }

    public void AtualizarGrid(String tipo) {
        lP = Client.CarregarProduto();
        DefaultTableModel grid = (DefaultTableModel) Tabela.getModel();
        int aux = grid.getRowCount();
        for (int i = 0; i < aux; i++) {
            grid.removeRow(0);
        }
        if (tipo == "Ordem Crescente") {
            if (Cb_tipo.getSelectedIndex() == 0) {
                for (int i = 0; i < lP.size(); i++) {

                    if (lP.get(i).getNome().toLowerCase().contains(Text_Pesquisa.getText().toLowerCase())
                            || Tipo(lP.get(i).getTipo()).toLowerCase().contains(Text_Pesquisa.getText().toLowerCase())
                            || String.valueOf(lP.get(i).getId()).toLowerCase().contains(Text_Pesquisa.getText().toLowerCase())) {
                        grid.addRow(new String[]{
                            String.valueOf(lP.get(i).getId()),
                            lP.get(i).getNome(),
                            Tipo(lP.get(i).getTipo()),
                            String.valueOf(lP.get(i).getQuantidade()),
                            String.valueOf(lP.get(i).getVenda())

                        });
                    }

                }
            } else {
                for (int i = 0; i < lP.size(); i++) {

                    if ((lP.get(i).getNome().toLowerCase().contains(Text_Pesquisa.getText().toLowerCase())
                            || String.valueOf(lP.get(i).getId()).toLowerCase().contains(Text_Pesquisa.getText().toLowerCase()))
                            && lP.get(i).getTipo() == Cb_tipo.getSelectedIndex() - 1) {
                        grid.addRow(new String[]{
                            String.valueOf(lP.get(i).getId()),
                            lP.get(i).getNome(),
                            Tipo(lP.get(i).getTipo()),
                            String.valueOf(lP.get(i).getQuantidade()),
                            String.valueOf(lP.get(i).getVenda())

                        });
                    }

                }
            }
        } else {
            if (Cb_tipo.getSelectedIndex() == 0) {
                for (int i = lP.size() - 1; i >= 0; i--) {

                    if (lP.get(i).getNome().toLowerCase().contains(Text_Pesquisa.getText().toLowerCase())
                            || Tipo(lP.get(i).getTipo()).toLowerCase().contains(Text_Pesquisa.getText().toLowerCase())
                            || String.valueOf(lP.get(i).getId()).toLowerCase().contains(Text_Pesquisa.getText().toLowerCase())) {
                        grid.addRow(new String[]{
                            String.valueOf(lP.get(i).getId()),
                            lP.get(i).getNome(),
                            Tipo(lP.get(i).getTipo()),
                            String.valueOf(lP.get(i).getQuantidade()),
                            String.valueOf(lP.get(i).getVenda())

                        });
                    }

                }
            } else {
                for (int i = lP.size() - 1; i >= 0; i--) {

                    if ((lP.get(i).getNome().toLowerCase().contains(Text_Pesquisa.getText().toLowerCase())
                            || String.valueOf(lP.get(i).getId()).toLowerCase().contains(Text_Pesquisa.getText().toLowerCase()))
                            && lP.get(i).getTipo() == Cb_tipo.getSelectedIndex() - 1) {
                        grid.addRow(new String[]{
                            String.valueOf(lP.get(i).getId()),
                            lP.get(i).getNome(),
                            Tipo(lP.get(i).getTipo()),
                            String.valueOf(lP.get(i).getQuantidade()),
                            String.valueOf(lP.get(i).getVenda())

                        });
                    }

                }
            }
        }
    }

